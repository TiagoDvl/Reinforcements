package shared.classes;

public class ClassAlfa {

    private int a;
    private String b;

    public ClassAlfa(int a, String b) {
        this.a = a;
        this.b = b;
    }

    public void showClassAlfaStatements() {
        System.out.println("Int is: " + a + " and String is: " + b);
    }
}

